                                                            <li><strong>Integrasi Luas</strong>: mendukung lebih dari
                                                                200 aplikasi dan API.</li>
                                                            <li><strong>Fleksibel</strong>: mendukung <em>conditional
                                                                    logic</em>, <em>branching</em>, hingga <em>custom
                                                                    scripting</em>.</li>
                                                            <li><strong>Low-code</strong>: cocok bagi non-developer
                                                                namun tetap powerful bagi developer berpengalaman.</li>
                                                        </ul>
                                                        <hr />
                                                    </section>

