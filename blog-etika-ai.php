                                                <h3>Kesimpulan</h3>
                                                <p>
                                                    <strong>Etika AI</strong> adalah isu penting di era digital.
                                                    Tantangan seperti privasi data, bias algoritma, hingga dampak sosial
                                                    perlu diantisipasi dengan regulasi, transparansi, dan pendekatan
                                                    human-centered.
