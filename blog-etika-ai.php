                                                    <li><strong>Transparan</strong> dalam cara kerja dan penggunaannya.
                                                    </li>
                                                    <li><strong>Aman</strong> dengan standar keamanan data tinggi.</li>
                                                    <li><strong>Humanis</strong> dengan menempatkan manusia sebagai
                                                        partner utama AI.</li>
                                                </ul>
                                                <p>
                                                    Dengan prinsip <strong>Inovasi, Kolaborasi, dan Integritas</strong>,
                                                    Getcore.id percaya bahwa AI dapat menjadi teknologi yang memajukan
                                                    tanpa mengorbankan nilai etika.
                                                </p>
